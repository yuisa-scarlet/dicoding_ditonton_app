import 'dart:convert';

import 'package:ditonton/data/models/movie_detail_model.dart';
import 'package:ditonton/data/models/movie_model.dart';
import 'package:ditonton/data/models/movie_response.dart';
import 'package:ditonton/common/exception.dart';
import 'package:http/http.dart' as http;

abstract class MovieRemoteDataSource {
  Future<List<MovieModel>> getNowPlayingMovies();
  Future<List<MovieModel>> getPopularMovies();
  Future<List<MovieModel>> getTopRatedMovies();
  Future<MovieDetailResponse> getMovieDetail(int id);
  Future<List<MovieModel>> getMovieRecommendations(int id);
  Future<List<MovieModel>> searchMovies(String query);
}

class MovieRemoteDataSourceImpl implements MovieRemoteDataSource {
  static const _baseUrl = 'https://api.themoviedb.org/3';

  final http.Client client;
  final String apiKey;

  MovieRemoteDataSourceImpl({
    required this.client,
    String? apiKey,
  }) : apiKey = apiKey ?? const String.fromEnvironment('TMDB_API_KEY');

  String get _apiKeyQuery {
    if (apiKey.isEmpty) {
      throw ArgumentError(
        'TMDB_API_KEY is missing. Run with --dart-define=TMDB_API_KEY=<your_key>',
      );
    }
    return 'api_key=$apiKey';
  }

  @override
  Future<List<MovieModel>> getNowPlayingMovies() async {
    final response = await client
        .get(Uri.parse('$_baseUrl/movie/now_playing?$_apiKeyQuery'));

    if (response.statusCode == 200) {
      return MovieResponse.fromJson(json.decode(response.body)).movieList;
    } else {
      throw ServerException();
    }
  }

  @override
  Future<MovieDetailResponse> getMovieDetail(int id) async {
    final response =
        await client.get(Uri.parse('$_baseUrl/movie/$id?$_apiKeyQuery'));

    if (response.statusCode == 200) {
      return MovieDetailResponse.fromJson(json.decode(response.body));
    } else {
      throw ServerException();
    }
  }

  @override
  Future<List<MovieModel>> getMovieRecommendations(int id) async {
    final response = await client
        .get(Uri.parse('$_baseUrl/movie/$id/recommendations?$_apiKeyQuery'));

    if (response.statusCode == 200) {
      return MovieResponse.fromJson(json.decode(response.body)).movieList;
    } else {
      throw ServerException();
    }
  }

  @override
  Future<List<MovieModel>> getPopularMovies() async {
    final response =
        await client.get(Uri.parse('$_baseUrl/movie/popular?$_apiKeyQuery'));

    if (response.statusCode == 200) {
      return MovieResponse.fromJson(json.decode(response.body)).movieList;
    } else {
      throw ServerException();
    }
  }

  @override
  Future<List<MovieModel>> getTopRatedMovies() async {
    final response =
        await client.get(Uri.parse('$_baseUrl/movie/top_rated?$_apiKeyQuery'));

    if (response.statusCode == 200) {
      return MovieResponse.fromJson(json.decode(response.body)).movieList;
    } else {
      throw ServerException();
    }
  }

  @override
  Future<List<MovieModel>> searchMovies(String query) async {
    final response = await client
        .get(Uri.parse('$_baseUrl/search/movie?$_apiKeyQuery&query=$query'));

    if (response.statusCode == 200) {
      return MovieResponse.fromJson(json.decode(response.body)).movieList;
    } else {
      throw ServerException();
    }
  }
}
